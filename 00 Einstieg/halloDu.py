# Die Frage "Wie heißt du?" wird angezeigt und die Antwort in der Variable name gespeichert
name = input("Wie heißt du?")

# Eine Begrüßung wird ausgegeben.
# Mit dem f vor dem String wird ein sog. formatted String erzeugt.
# Bei formatted Strings können Variablen in geschweiften Klammern direkt eingebunden werden.
print(f"Hallo {name}!")
